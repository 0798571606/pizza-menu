function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Co.</h1>
    </header>
  );
}

function Menu() {
  const pizzas = pizzaData;
  return (
    <main className="pizzas">
      {pizzas.map((pizza) => (
        <Pizza pizzaObg={pizza} key={pizza.name} />
      ))}
    </main>
  );
}

function Pizza({ pizzaObg }) {
  return (
    <li className={`pizza ${pizzaObg.soldOut ? 'sold-out' : ''}`}>
      <img src={pizzaObg.photoName} alt="hamid" />
      <div>
        <h3>{pizzaObg.name}</h3>
        <p>{pizzaObg.ingredients}</p>
        <span>{pizzaObg.soldOut ? 'SOLD OUT' : pizzaObg.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 8;
  const closeHour = 20;
  const isOpen = hour >= openHour && hour <= closeHour;
  return (
    <footer className="footer">
      {isOpen && <Order openHour={openHour} closeHour={closeHour} />}
    </footer>
  );
}

function Order({ openHour, closeHour }) {
  return (
    <div className="order">
      <p>
        We're open from {openHour}:00 until {closeHour}:00. Come visit us or order online!
      </p>
      <button
        className="btn"
        onClick={() => alert('Hi dear please choose your favorite pizza first!')}
      >
        Order
      </button>
    </div>
  );
}

const root = ReactDOM.createRoot(document.getElementById('root'));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
